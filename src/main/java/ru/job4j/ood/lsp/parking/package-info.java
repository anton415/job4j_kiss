/**
 * Задача по моделированию парковки для легковых и грузовых машин.
 *
 * <h2>Схема взаимодействия интерфейсов</h2>
 * <pre>
 * Клиент -> Parking.park(Car)
 * Parking -> Car.getSize()
 *
 * Если размер == 1:
 * Parking резервирует 1 место для легковых машин.
 *
 * Если размер > 1:
 * Parking сначала пытается занять 1 грузовое место,
 * а если его нет - N соседних легковых мест, где N = размер машины.
 *
 * После успешной парковки Parking обновляет внутренний учет занятых мест
 * и делает машину доступной через Parking.findAll().
 * </pre>
 *
 * <p>
 * Контракты пакета специально сведены к двум ролям:
 * {@link ru.job4j.ood.lsp.parking.Car} описывает машину,
 * {@link ru.job4j.ood.lsp.parking.Parking} описывает сервис парковки и учета.
 * Реализация алгоритма размещения будет добавлена на следующем этапе.
 * </p>
 */
